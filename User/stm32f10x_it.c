/* UART DMA RX buffer */
#define UART_RX_LEN     128
extern uint8_t Uart_Rx[UART_RX_LEN];
/* USART1_IRQ Handler */
void USART1_IRQHandler(void)
{
    uint32_t temp = 0;
    uint16_t i = 0;

    if(USART_GetITStatus(USART1, USART_IT_IDLE) != RESET)
    {
        //USART_ClearFlag(USART1,USART_IT_IDLE);
        temp = USART1->SR;
        temp = USART1->DR;  /* clear USART_IT_IDLE bit */
        DMA_Cmd(DMA1_Channel5,DISABLE);

        temp = UART_RX_LEN - DMA_GetCurrDataCounter(DMA1_Channel5);  /* get DMA RX len */
        for (i = 0;i < temp;i++)
        {
#if 0
        	/* in_queue */
            Data_Receive_Usart = Uart_Rx[i];
            /* RX handle */
            usart_state_run();
#endif
        }

        /* reset RX len */
        DMA_SetCurrDataCounter(DMA1_Channel5,UART_RX_LEN);
        /* enable DMA RX */
        DMA_Cmd(DMA1_Channel5,ENABLE);
    }

    __nop();
}

